function testCalendar() {
	
	var fails = 0;
	
	for (var i in testdata) {
		
		//0123456789012345678901
		//YYYY-MM-DD = YYYY-WW-D
		var ds = testdata[i];
		var sy = ds.substring(0,4);
		var sm = ds.substring(5,7);
		var sd = ds.substring(8,10);

		var dy = 1*ds.substring(13,17);
		var dw = 1*ds.substring(18,20);
		var dd = 1*ds.substring(21,22);

		var cal = new ISOCalendar(sy,sm-1,sd);
		if (cal.getIsoYear()!=dy) {
			document.write(sy+"-"+sm+"-"+sd+" -> year: <b>"+dy+" != "+cal.getIsoYear()+"</b><br/>");
			fails++;
		}
		if (cal.getIsoWeek()!=dw) {
			document.write(sy+"-"+sm+"-"+sd+" -> week: <b>"+dw+" != "+cal.getIsoWeek()+"</b><br/>");
			fails++;
		}
		if (cal.getWeekDay()!=dd) {
			document.write(sy+"-"+sm+"-"+sd+" -> weekDay: <b>"+dd+" != "+cal.getWeekDay()+"</b><br/>");
			fails++;
		}
	}
	document.write("<p>iso year/week: "+fails+" / "+(testdata.length*3)+" test(s) failed</p><hr/>");
	
	for (var year=2019; year<2021; year++) {
		for (var month=0; month<12; month++) {
			document.write("<p>"+year+"/"+(month+1)+"</p>");
			var start = new ISOCalendar(year, month).getMonday();
			var end = new ISOCalendar(year, month).getLastDayOfMonth().getFriday();
			document.write("<p>"+start.toDate()+" -> "+end.toDate()+"</p>");
		}
	}
	
}